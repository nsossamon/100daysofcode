# If/Else is a Conditional statement
# draw.io is good to produce flow charts and diagrams for logic

print("Welcome to the rollercoaster!")
height = int(input("What is your height in cm? "))

if height > 120:                                                     # if/else are a pair, put them on same indentation
    print("You can ride the rollercoaster!")
else:
    print("Sorry you're a short little bitch and can't ride this!")

